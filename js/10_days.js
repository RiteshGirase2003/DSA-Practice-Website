function renderContent() {
    const contentDiv = document.getElementById('content');

    for (const day in data) {
        const dayData = data[day];

        // Create the main topic card
        const topicCard = document.createElement('div');
        topicCard.classList.add('step-card'); // Use step-card class for styling

        const topicTitle = document.createElement('h2');
        topicTitle.textContent = dayData.des;
        topicCard.appendChild(topicTitle);

        // Create a table for problems (initially hidden)
        const table = document.createElement('table');
        table.style.display = 'none'; // Hide table initially

        // Create table header
        const headerRow = document.createElement('tr');
        const header1 = document.createElement('th');
        header1.textContent = 'Title';
        const header2 = document.createElement('th');
        header2.textContent = 'Platform';
        const header3 = document.createElement('th');
        header3.textContent = 'Link';
        const header4 = document.createElement('th');
        header4.textContent = 'Difficulty';
        headerRow.appendChild(header1);
        headerRow.appendChild(header2);
        headerRow.appendChild(header3);
        headerRow.appendChild(header4);
        table.appendChild(headerRow);

        // Populate the table with problems
        dayData.problems.forEach(problem => {
            const problemRow = document.createElement('tr');

            const titleCell = document.createElement('td');
            titleCell.textContent = problem.title;

            const platformCell = document.createElement('td');
            platformCell.textContent = problem.platform;

            const linkCell = document.createElement('td');
            const problemLink = document.createElement('a');
            problemLink.href = problem.link;
            problemLink.textContent = "Link"; // Display "Link" text
            problemLink.target = '_blank'; // Open in a new tab
            linkCell.appendChild(problemLink);

            const difficultyCell = document.createElement('td');
            difficultyCell.textContent = problem.difficulty;

            problemRow.appendChild(titleCell);
            problemRow.appendChild(platformCell);
            problemRow.appendChild(linkCell);
            problemRow.appendChild(difficultyCell);
            table.appendChild(problemRow);
        });

        topicCard.appendChild(table); // Append the table to the topic card
        contentDiv.appendChild(topicCard); // Append the topic card to the main content

        // Add click event to toggle card
        topicCard.addEventListener('click', function() {
            // Check if the card is already expanded
            const isExpanded = table.style.display === 'table'; // Check if table is visible

            // Close all other cards
            const allCards = document.querySelectorAll('.step-card');
            allCards.forEach(card => {
                const cardTable = card.querySelector('table');
                if (card !== topicCard) {
                    cardTable.style.display = 'none'; // Close other cards
                }
            });

            // Toggle the clicked card
            if (!isExpanded) {
                table.style.display = 'table'; // Expand the clicked card
            }
        });
    }
}

renderContent();
